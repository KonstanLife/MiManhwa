package com.example.mimanhwa.Models;

public class ModelChapter {

    String uid,id,title,bookId,url;
    long timestamp;

    public ModelChapter(){

    }

    public ModelChapter(String uid, String id, String title, String bookId, String url, long timestamp) {
        this.uid = uid;
        this.id = id;
        this.title = title;
        this.bookId = bookId;
        this.url = url;
        this.timestamp = timestamp;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBookId() {
        return bookId;
    }

    public void setBookId(String bookId) {
        this.bookId = bookId;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }
}
